#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

/*
A função escreve o caracter c no file descriptor fd

File descriptor é usado como parametro para especificar em
que ficheiro escrever.
*/
